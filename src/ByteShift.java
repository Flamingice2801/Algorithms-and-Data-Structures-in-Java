public class ByteShift {

    public static void main(String[] strings){
        int k =-29;
        System.out.println(k << 1);
        System.out.println(k >> 1);
        System.out.println(k >>> 1);

        System.out.println(Integer.toBinaryString(5));

        System.out.println(Integer.toBinaryString(-1));

        System.out.println(~5);
    }
}
